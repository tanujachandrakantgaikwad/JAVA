//7). Write a Java program to display characters from A to Z using loop. 
class Alphabets 
{
    public static void main(String[] args)
 {
        System.out.println("Alphabets from A to Z:");
        
        for (char ch = 'A'; ch <= 'Z'; ch++)
      {
            System.out.print(ch + " ");
        }
    }
}
